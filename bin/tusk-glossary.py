#!/usr/bin/env python3
"""Manage the project glossary.

Called by the tusk wrapper:
    tusk glossary list|get|search|add|remove|set-definition|sync-from-md|export ...

Arguments received from tusk:
    sys.argv[1] — DB path
    sys.argv[2] — config path
    sys.argv[3:] — subcommand + flags
"""

import argparse
import json
import os
import re
import sqlite3
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
import tusk_loader  # loads tusk-db-lib.py and tusk-json-lib.py

_db_lib = tusk_loader.load("tusk-db-lib")
_json_lib = tusk_loader.load("tusk-json-lib")
dumps = _json_lib.dumps
get_connection = _db_lib.get_connection
load_config = _db_lib.load_config


# ── Markdown parsing ─────────────────────────────────────────────────

def parse_glossary_md(content: str) -> list:
    """Extract ``[(term, definition, see_also)]`` triples from a GLOSSARY.md doc.

    Sections are headed ``## term``. The definition is the first non-empty
    paragraph after the heading. The optional ``→ See …`` line that follows
    is captured as ``see_also`` (with the leading arrow stripped). Sections
    without a definition body are skipped.
    """
    entries = []
    sections = re.split(r"(?m)^## ", content)
    for section in sections[1:]:
        lines = section.splitlines()
        if not lines:
            continue
        term = lines[0].strip()
        if not term:
            continue
        body_lines = lines[1:]
        definition_parts = []
        see_also = None
        for line in body_lines:
            stripped = line.strip()
            if stripped == "---":
                break
            if stripped.startswith("→ See"):
                see_also_text = stripped[len("→ See"):].lstrip()
                see_also = see_also_text.rstrip(".").strip() or None
                continue
            definition_parts.append(line)
        definition = "\n".join(definition_parts).strip()
        if not definition:
            continue
        entries.append((term, definition, see_also))
    return entries


def _default_md_path(db_path: str) -> str:
    repo_root = os.path.dirname(os.path.dirname(os.path.abspath(db_path)))
    return os.path.join(repo_root, "docs", "GLOSSARY.md")


def _normalize_topics(raw: str | None) -> str | None:
    if raw is None:
        return None
    parts = [p.strip() for p in raw.split(",") if p.strip()]
    return ",".join(parts) if parts else None


def render_glossary_md(rows: list) -> str:
    """Render the ``glossary`` table back to markdown.

    Each row is a tuple/sequence ``(term, definition, see_also, topics)``.
    The output starts with a ``<!-- generated -->`` header so editors know
    not to hand-edit the file.
    """
    out = [
        "<!-- generated by `tusk glossary export` — edit via `tusk glossary` CLI -->",
        "",
        "# Tusk Glossary",
        "",
        "Canonical definitions for terms used across tusk documentation and skills.",
        "",
        "---",
        "",
    ]
    for i, row in enumerate(rows):
        term = row[0]
        definition = row[1]
        see_also = row[2] if len(row) > 2 else None
        out.append(f"## {term}")
        out.append("")
        out.append(definition)
        out.append("")
        if see_also:
            out.append(f"→ See {see_also}.")
            out.append("")
        if i != len(rows) - 1:
            out.append("---")
            out.append("")
    return "\n".join(out).rstrip() + "\n"


# ── Subcommands ──────────────────────────────────────────────────────

def cmd_list(args: argparse.Namespace, db_path: str, config: dict) -> int:
    conn = get_connection(db_path)
    try:
        rows = conn.execute(
            "SELECT id, term, definition, see_also, topics "
            "FROM glossary ORDER BY term COLLATE NOCASE"
        ).fetchall()
    finally:
        conn.close()
    result = [
        {
            "id": r[0],
            "term": r[1],
            "definition": r[2],
            "see_also": r[3],
            "topics": r[4],
        }
        for r in rows
    ]
    print(dumps(result))
    return 0


def cmd_get(args: argparse.Namespace, db_path: str, config: dict) -> int:
    conn = get_connection(db_path)
    try:
        row = conn.execute(
            "SELECT id, term, definition, see_also, topics "
            "FROM glossary WHERE term = ?",
            (args.term,),
        ).fetchone()
    finally:
        conn.close()
    if row is None:
        print(f"Error: glossary term '{args.term}' not found", file=sys.stderr)
        return 1
    print(dumps({
        "id": row[0],
        "term": row[1],
        "definition": row[2],
        "see_also": row[3],
        "topics": row[4],
    }))
    return 0


def cmd_search(args: argparse.Namespace, db_path: str, config: dict) -> int:
    pattern = f"%{args.query}%"
    conn = get_connection(db_path)
    try:
        rows = conn.execute(
            "SELECT id, term, definition, see_also, topics FROM glossary "
            "WHERE term LIKE ? OR definition LIKE ? OR topics LIKE ? "
            "ORDER BY term COLLATE NOCASE",
            (pattern, pattern, pattern),
        ).fetchall()
    finally:
        conn.close()
    result = [
        {
            "id": r[0],
            "term": r[1],
            "definition": r[2],
            "see_also": r[3],
            "topics": r[4],
        }
        for r in rows
    ]
    print(dumps(result))
    return 0


def cmd_add(args: argparse.Namespace, db_path: str, config: dict) -> int:
    topics = _normalize_topics(args.topics)
    conn = get_connection(db_path)
    try:
        conn.execute(
            "INSERT INTO glossary (term, definition, see_also, topics) "
            "VALUES (?, ?, ?, ?)",
            (args.term, args.definition, args.see_also, topics),
        )
        conn.commit()
        new_id = conn.execute("SELECT last_insert_rowid()").fetchone()[0]
        print(json.dumps({"id": new_id, "term": args.term}))
        return 0
    except sqlite3.IntegrityError as e:
        if "UNIQUE constraint" in str(e):
            print(f"Error: glossary term '{args.term}' already exists", file=sys.stderr)
        else:
            print(f"Error: {e}", file=sys.stderr)
        return 1
    finally:
        conn.close()


def cmd_remove(args: argparse.Namespace, db_path: str, config: dict) -> int:
    conn = get_connection(db_path)
    try:
        cur = conn.execute("DELETE FROM glossary WHERE term = ?", (args.term,))
        conn.commit()
        if cur.rowcount == 0:
            print(f"Error: glossary term '{args.term}' not found", file=sys.stderr)
            return 1
        if sys.stdout.isatty():
            print(f"Removed glossary term '{args.term}'")
        return 0
    finally:
        conn.close()


def cmd_set_definition(args: argparse.Namespace, db_path: str, config: dict) -> int:
    if args.definition is None and args.see_also is None and args.topics is None:
        print(
            "Error: at least one of --definition, --see-also, or --topics is required",
            file=sys.stderr,
        )
        return 1

    fields, values = [], []
    if args.definition is not None:
        fields.append("definition = ?")
        values.append(args.definition)
    if args.see_also is not None:
        fields.append("see_also = ?")
        values.append(args.see_also or None)
    if args.topics is not None:
        fields.append("topics = ?")
        values.append(_normalize_topics(args.topics))
    fields.append("updated_at = datetime('now')")
    values.append(args.term)

    conn = get_connection(db_path)
    try:
        cur = conn.execute(
            f"UPDATE glossary SET {', '.join(fields)} WHERE term = ?",
            values,
        )
        conn.commit()
        if cur.rowcount == 0:
            print(f"Error: glossary term '{args.term}' not found", file=sys.stderr)
            return 1
        if sys.stdout.isatty():
            print(f"Updated glossary term '{args.term}'")
        return 0
    finally:
        conn.close()


def cmd_sync_from_md(args: argparse.Namespace, db_path: str, config: dict) -> int:
    """Upsert glossary entries from a markdown source-of-truth doc.

    Default source: ``<repo_root>/docs/GLOSSARY.md``. When the file is absent
    the command is a no-op and exits 0 — target projects without a glossary
    file keep whatever the table already contains.
    """
    md_path = args.file or _default_md_path(db_path)

    if not os.path.isfile(md_path):
        print(dumps({
            "source": md_path,
            "found": False,
            "parsed": 0,
            "added": [],
            "updated": [],
            "unchanged": [],
        }))
        return 0

    with open(md_path, "r", encoding="utf-8") as f:
        content = f.read()

    entries = parse_glossary_md(content)

    added, updated, unchanged = [], [], []
    conn = get_connection(db_path)
    try:
        for term, definition, see_also in entries:
            row = conn.execute(
                "SELECT definition, see_also FROM glossary WHERE term = ?",
                (term,),
            ).fetchone()
            if row is None:
                conn.execute(
                    "INSERT INTO glossary (term, definition, see_also) "
                    "VALUES (?, ?, ?)",
                    (term, definition, see_also),
                )
                added.append(term)
            elif row[0] != definition or row[1] != see_also:
                conn.execute(
                    "UPDATE glossary SET definition = ?, see_also = ?, "
                    "updated_at = datetime('now') WHERE term = ?",
                    (definition, see_also, term),
                )
                updated.append(term)
            else:
                unchanged.append(term)
        conn.commit()
    finally:
        conn.close()

    print(dumps({
        "source": md_path,
        "found": True,
        "parsed": len(entries),
        "added": added,
        "updated": updated,
        "unchanged": unchanged,
    }))
    return 0


def cmd_export(args: argparse.Namespace, db_path: str, config: dict) -> int:
    """Render the glossary table back to markdown.

    By default writes to ``<repo_root>/docs/GLOSSARY.md`` (or the
    ``--file`` override). With ``--stdout`` the rendered markdown is
    printed instead — used by ``tusk lint`` rule 26 to detect drift
    without touching the file.
    """
    md_path = args.file or _default_md_path(db_path)
    conn = get_connection(db_path)
    try:
        rows = conn.execute(
            "SELECT term, definition, see_also, topics "
            "FROM glossary ORDER BY term COLLATE NOCASE"
        ).fetchall()
    finally:
        conn.close()
    rendered = render_glossary_md([tuple(r) for r in rows])
    if args.stdout:
        sys.stdout.write(rendered)
        return 0
    os.makedirs(os.path.dirname(md_path), exist_ok=True)
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(rendered)
    print(dumps({"path": md_path, "entries": len(rows)}))
    return 0


# ── Argument parsing ─────────────────────────────────────────────────

def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="tusk glossary",
        description="Manage project glossary entries",
    )
    sub = parser.add_subparsers(dest="subcommand", required=True)

    sub.add_parser("list", help="List all glossary entries as JSON")

    p_get = sub.add_parser("get", help="Get a single glossary entry by term")
    p_get.add_argument("term", help="Term to fetch")

    p_search = sub.add_parser(
        "search",
        help="Search glossary by term, definition, or topic (substring match)",
    )
    p_search.add_argument("query", help="Search string")

    p_add = sub.add_parser("add", help="Add a new glossary entry")
    p_add.add_argument("term", help="Term name (must be unique)")
    p_add.add_argument("definition", help="Definition text")
    p_add.add_argument("--see-also", default=None, help="Optional see-also reference")
    p_add.add_argument("--topics", default=None, help="Comma-separated topic tags")

    p_remove = sub.add_parser("remove", help="Remove a glossary entry by term")
    p_remove.add_argument("term", help="Term to remove")

    p_set = sub.add_parser(
        "set-definition",
        help="Update an existing glossary entry's definition, see-also, or topics",
    )
    p_set.add_argument("term", help="Term to update")
    p_set.add_argument("--definition", default=None, help="New definition text")
    p_set.add_argument("--see-also", default=None, help="New see-also reference")
    p_set.add_argument("--topics", default=None, help="New comma-separated topic tags")

    p_sync = sub.add_parser(
        "sync-from-md",
        help="Upsert glossary entries from docs/GLOSSARY.md (idempotent)",
    )
    p_sync.add_argument(
        "--file",
        default=None,
        help="Override markdown source path (default: <repo_root>/docs/GLOSSARY.md)",
    )

    p_export = sub.add_parser(
        "export",
        help="Render the glossary table back to docs/GLOSSARY.md",
    )
    p_export.add_argument(
        "--file",
        default=None,
        help="Override output path (default: <repo_root>/docs/GLOSSARY.md)",
    )
    p_export.add_argument(
        "--stdout",
        action="store_true",
        help="Write rendered markdown to stdout instead of the file",
    )

    return parser


# ── Entry point ───────────────────────────────────────────────────────

def main() -> None:
    if len(sys.argv) < 4:
        print(
            "Usage: tusk glossary {list|get|search|add|remove|set-definition|sync-from-md|export} ...",
            file=sys.stderr,
        )
        sys.exit(1)

    db_path = sys.argv[1]
    config_path = sys.argv[2]
    rest = sys.argv[3:]

    config = load_config(config_path)

    parser = build_parser()
    args = parser.parse_args(rest)

    handlers = {
        "list": cmd_list,
        "get": cmd_get,
        "search": cmd_search,
        "add": cmd_add,
        "remove": cmd_remove,
        "set-definition": cmd_set_definition,
        "sync-from-md": cmd_sync_from_md,
        "export": cmd_export,
    }
    sys.exit(handlers[args.subcommand](args, db_path, config))


if __name__ == "__main__":
    main()
